public class Value2 
{
	/* method overloading-multiple method can have 
	same name with different parameter */	
	
	public int add(int x,int y)
	{
		return x+y;
	}
	
	public float add(float x,float y)
	{
		return x*y;
		
	}
	public double add(double x,double y)
	{
		return x/y;
	}
	
}
